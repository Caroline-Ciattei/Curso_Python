"""
 Tipo None

 O tipo de dado None em Python representa o tipo sem tipo, ou poderia ser conhecido também
 como tipo vazio, porém, falar tipo sem tipo é mais apropiado.

 O tipo None é SEMPRE representado com a primeira letra maiúscula.

 Certo: None
 Errado: none

 Quando utilizamos?

 - Podemos utilizar None quando queremos criar uma variável e inicializá-la com um tipo sem tipo, antes
 de recebermos um valor final.

 OBS: O tipo None em Pyhton é SEMPRE considerado false.
"""
numero = None

print(numero)
print(type(numero))

numero = 1.44, 1.34, 5.67

print(numero)
print(type(numero))